        await db.SaveChangesAsync(cancellationToken);
    }

    private static async Task EnsureSalesInvoiceTemplateV2Async(ImportDbContext db, CancellationToken cancellationToken)
    {
        var salesType = await db.ImportFileTypes
            .FirstOrDefaultAsync(x => x.Code == ImportFileTypeCodes.SalesInvoice, cancellationToken);
        if (salesType is null)
        {
            return;
        }

        var template = await db.ImportTemplates
            .Include(x => x.ColumnMappings)
                .ThenInclude(x => x.TransformRules)
            .FirstOrDefaultAsync(
                x => x.ImportFileTypeId == salesType.Id && x.Name == "Template NF Saida - Itens de Venda",
                cancellationToken);

        if (template is null)
        {
            template = new ImportTemplate
            {
                ImportFileTypeId = salesType.Id,
                Name = "Template NF Saida - Itens de Venda",
                Description = "Template para ITEM DE VENDA (NOTAS FISCAIS DE SAIDA).xlsx",
                IsActive = true,
                FileNamePattern = "ITEM DE VENDA(NOTAS FISCAIS DE SAIDA);ITEM DE VENDA",
                RequiredHeadersCsv = "documento,data,cliente,nome,produto,descricao,quantidade,vlr. unit.,total,tipo,cidade,grupo descricao,peso bruto(kg)",
                CreatedAt = DateTime.UtcNow,
                UpdatedAt = DateTime.UtcNow
            };
            db.ImportTemplates.Add(template);
            await db.SaveChangesAsync(cancellationToken);
        }
        else
        {
            template.Description = "Template para ITEM DE VENDA (NOTAS FISCAIS DE SAIDA).xlsx";
            template.IsActive = true;
            template.FileNamePattern = "ITEM DE VENDA(NOTAS FISCAIS DE SAIDA);ITEM DE VENDA";
            template.RequiredHeadersCsv = "documento,data,cliente,nome,produto,descricao,quantidade,vlr. unit.,total,tipo,cidade,grupo descricao,peso bruto(kg)";
            template.UpdatedAt = DateTime.UtcNow;
        }

        var rulesByCode = await db.TransformRules
            .Where(x => x.IsActive)
            .ToDictionaryAsync(x => x.Code, StringComparer.OrdinalIgnoreCase, cancellationToken);

        UpsertColumnMapping(
            template,
            "documento",
            "documentnumber",
            true,
            null,
            [("Trim", 1, null)],
            rulesByCode);
        UpsertColumnMapping(
            template,
            "data",
            "transactiondate",
            true,
            null,
            [("Trim", 1, null), ("BrazilianDate", 2, """{"formats":["yyyyMMdd","dd/MM/yyyy","yyyy-MM-dd","MM/dd/yyyy"]}""")],
            rulesByCode);
        UpsertColumnMapping(
            template,
            "cliente",
            "customercode",
            true,
            null,
            [("Trim", 1, null)],
            rulesByCode);
        UpsertColumnMapping(
            template,
            "nome",
            "customername",
            true,
            null,
            [("Trim", 1, null)],
            rulesByCode);
        UpsertColumnMapping(
            template,
            "produto",
            "productcode",
            true,
            null,
            [("Trim", 1, null)],
            rulesByCode);
        UpsertColumnMapping(
            template,
            "descricao",
