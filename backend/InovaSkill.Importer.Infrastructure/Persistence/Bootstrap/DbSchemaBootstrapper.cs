    private static async Task EnsureDefaultTemplatesAsync(ImportDbContext db, CancellationToken cancellationToken)
    {
        var templates = await db.PreProcessorTemplates
            .Include(x => x.Rules)
            .ToListAsync(cancellationToken);

        var products = UpsertTemplate(
            db,
            templates,
            "default_products_v1",
            "Template Padrão Products",
            FileType.Products,
            "products",
            "sku,name,price");

        var orders = UpsertTemplate(
            db,
            templates,
            "default_orders_v1",
            "Template Padrão Orders",
            FileType.Orders,
            "orders",
            "ordernumber,customeremail,productsku,quantity,orderdate");

        var customers = UpsertTemplate(
            db,
            templates,
            "default_customers_v1",
            "Template Padrão Customers",
            FileType.Customers,
            "customers",
            "name,email");

        var commercialTransactions = UpsertTemplate(
            db,
            templates,
            "default_commercial_transactions_v1",
            "Template Padrão Vendas",
            FileType.CommercialTransaction,
            "vendas",
            "documentnumber,transactiondate,customercode,customername,productcode,productdescription,quantity,unitprice,totalamount,transactiontype,city,productgroup,grossweightkg");
        commercialTransactions.ColumnMappingsJson = """
        [
          { "From": "documento", "To": "documentnumber" },
          { "From": "data", "To": "transactiondate" },
          { "From": "cliente", "To": "customercode" },
          { "From": "nome", "To": "customername" },
          { "From": "produto", "To": "productcode" },
          { "From": "descricao", "To": "productdescription" },
          { "From": "quantidade", "To": "quantity" },
          { "From": "vlr. unit.", "To": "unitprice" },
          { "From": "total", "To": "totalamount" },
          { "From": "tipo", "To": "transactiontype" },
