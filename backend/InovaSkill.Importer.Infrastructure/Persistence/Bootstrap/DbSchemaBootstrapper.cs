    private static async Task EnsureTransformRulesCatalogAsync(ImportDbContext db, CancellationToken cancellationToken)
    {
        var catalog = await db.TransformRules.ToListAsync(cancellationToken);

        UpsertTransformRule(catalog, db, "Trim", "Trim", "Remove espacos no inicio/fim.");
        UpsertTransformRule(catalog, db, "OnlyDigits", "Somente Digitos", "Mantem apenas digitos.");
        UpsertTransformRule(catalog, db, "BrazilianCurrency", "Moeda BR", "Converte texto monetario pt-BR para decimal.");
        UpsertTransformRule(catalog, db, "BrazilianDate", "Data BR", "Converte texto de data para DateTime.");
        UpsertTransformRule(catalog, db, "UpperCase", "Maiusculo", "Converte texto para maiusculo.");
